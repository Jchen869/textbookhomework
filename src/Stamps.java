public class Stamps {
    public static void main (String[] args){

        int susan = 8;
        int jean = 40;
        System.out.println("40 - 8"); //Sets up the equation
        System.out.println(jean - susan); //Calculates the equation
        System.out.println("Jean has " + (jean - susan) + " more stamps than Susan.");
        //Says how many more stamps does Jean have

    }
}
